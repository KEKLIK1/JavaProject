package task_23;

/**
 * Интерфейс очереди (FIFO - First In First Out)
 *
 * КОНТРАКТ:
 *
 * Инвариант:
 * 1. Размер очереди всегда неотрицателен: size() >= 0
 * 2. Первый элемент доступен только если очередь не пуста
 * 3. Элементы сохраняют порядок добавления (FIFO)
 * 4. Очередь должна корректно обрабатывать null-элементы (если не указано иное)
 *
 * Методы и их контракты:
 *
 * enqueue(element)
 * - Предусловие: элемент может быть null (если реализация разрешает)
 * - Постусловие: размер увеличивается на 1, элемент добавляется в конец
 * - Постусловие: element() возвращает тот же первый элемент, что и до вызова
 *
 * element()
 * - Предусловие: очередь не пуста (!isEmpty())
 * - Постусловие: возвращает первый элемент
 * - Постусловие: очередь не изменяется
 * - Постусловие: size() остается неизменным
 *
 * dequeue()
 * - Предусловие: очередь не пуста (!isEmpty())
 * - Постусловие: размер уменьшается на 1
 * - Постусловие: возвращает первый элемент, который был в очереди
 * - Постусловие: если в очереди был один элемент, после вызова isEmpty() == true
 *
 * size()
 * - Предусловие: нет
 * - Постусловие: возвращает количество элементов в очереди
 * - Постусловие: очередь не изменяется
 *
 * isEmpty()
 * - Предусловие: нет
 * - Постусловие: возвращает true если очередь пуста, иначе false
 * - Постусловие: очередь не изменяется
 *
 * clear()
 * - Предусловие: нет
 * - Постусловие: очередь становится пустой (isEmpty() == true)
 * - Постусловие: size() == 0
 */
public interface Queue {
    void enqueue(int element);
    int element();
    void dequeue();
    int size();
    boolean isEmpty();
    void clear();
}